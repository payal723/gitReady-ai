import fs from 'fs-extra';
import path from 'path';
import { getAIReadmeSummary, getInitialReadmeContent } from './ai.js';

export async function updateReadme(currentDir, diff) {
    const readmePath = path.join(currentDir, 'README.md');
    
    try {
        if (await fs.pathExists(readmePath)) {
            // If file exists, just append the update
            const summary = await getAIReadmeSummary(diff);
            const updateSection = `\n\n### Latest Updates (${new Date().toLocaleDateString()})\n- ${summary}`;
            await fs.appendFile(readmePath, updateSection);
        } else {
            // If file doesn't exist, create a FULL professional README
            const fullContent = await getInitialReadmeContent(diff);
            await fs.writeFile(readmePath, fullContent + `\n\n---\n*Generated by GitReady AI*`);
        }
        return true;
    } catch (error) {
        throw new Error("Failed to update README: " + error.message);
    }
}